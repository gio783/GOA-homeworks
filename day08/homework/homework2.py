age = int(input("შეიყვანეთ თქვენი ასაკი: "))
age_str = str(age)
print("თქვენი ასაკი როგორც სტრინგი:", age_str)
print("მისი ტიპია:", type(age_str))




# str() - გადაყავს სხვა ტიპის მნიშვნელობა სტრინგად (მაგ: რიცხვი ტექსტად)
# int() - გადაყავს სხვა ტიპის მნიშვნელობა მთლიან რიცხვად (მაგ: ტექსტი '25' → 25)
# float() - გადაყავს სხვა ტიპის მნიშვნელობა ათწილად რიცხვად (მაგ: '3.14' → 3.14)